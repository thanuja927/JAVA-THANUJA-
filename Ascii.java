public class Ascii {
    public static void main(String[] args) {
        char letterA = 'A';
        char letterB = 'B';
        char letterC = 'C';
        char letterD = 'D';
        char letterE = 'E';
        char letterF = 'F';
        
        int asciiA = (int) letterA;
        int asciiB = (int) letterB;
        int asciiC = (int) letterC;
        int asciiD = (int) letterD;
        int asciiE = (int) letterE;
        int asciiF = (int) letterF;

        System.out.println("ASCII value of " + letterA + ": " + asciiA);
        System.out.println("ASCII value of " + letterB + ": " + asciiB);
        System.out.println("ASCII value of " + letterC + ": " + asciiC);
        System.out.println("ASCII value of " + letterD + ": " + asciiD);
        System.out.println("ASCII value of " + letterE + ": " + asciiE);
        System.out.println("ASCII value of " + letterF + ": " + asciiF);
    }
}